package lv.javaguru.lessons.lesson1.homework;

public class ArithmeticOperations {

    public static void main(String[] args) {

        int firstNumber = 10;
        int secondNumber = 5;

        int multiplyResult = firstNumber * secondNumber;
        System.out.println("\nMultiply result = " + multiplyResult);

        int divideResult = firstNumber / secondNumber;
        System.out.println("\nDivide result = " + divideResult);

        int addResult = firstNumber + secondNumber;
        System.out.println("\nAdd result = " + addResult);

        int substractResult = firstNumber - secondNumber;
        System.out.println("\nSubstract result = " + substractResult);

    }


}
